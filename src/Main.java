public class Main {
    public static void main(String[] args) {
//        1. Вывести макс и мин
//        2. Вывести чет и не чет числа
//        3. Вывести самое длинное и короткое число
//        Со * 4. Выводить только те числа которые идут в порядке возрастания
//        Со * 5. Вывести все числа палиндромы в массиве
//===========================================================================================
        //Задача 1 Вывести макс и мин
        int[] arr = {4542, 78419, -1562, 12, 34, 6, 91, 78, -29, -14};
        int min = arr[0];
        int max = arr[0];

        for (int i = 1; i < arr.length; i++) {
            if (arr[i] < min) {
                min = arr[i]; //минимальное значение
            }
            if (arr[i] > max) {
                max = arr[i]; //максимальное значение
            }
        }
        System.out.println("Задача № 1");
        System.out.println("Минимальное значение: " + min);
        System.out.println("Максимальное значение: " + max);
//===========================================================================================
        //Задача 2 Вывести чет и не чет числа
        System.out.println("\nЗадача № 2");
        System.out.print("Чётные числа: ");
        for (int num : arr) { // цикл for-each
            if (num % 2 == 0) {  // Проверка на чётность
                System.out.print(num + " ");
            }
        }
        System.out.print("\nНечётные числа: ");
        for (int num : arr) { // цикл for-each
            if (num % 2 != 0) {  // Проверка на нечётность
                System.out.print(num + " ");
            }
        }
//===========================================================================================
//Задача 3 Вывести самое длинное и короткое число
        System.out.println("\n\nЗадача № 3");
        int maxL = 0; // Начальное значение для максимальной длины
        int minL = 10; // Начальное значение для минимальной длины (больше максимального количества цифр)
        int maxNum = arr[0]; // Переменная для хранения числа с максимальной длиной
        int minNum = arr[0]; // Переменная для хранения числа с минимальной длиной

        for (int num : arr) { // Проходим по каждому числу в массиве
            int length = (num == 0) ? 1 : 0; // Если число равно нулю, длина равна 1
            int tempNum = num; // Создаем временную переменную для работы с числом
            if (tempNum < 0) {// Если число отрицательное, убираем знак минус
                tempNum = -tempNum; // Убираем знак минус
            }
            // Подсчет количества цифр
            while (tempNum > 0) {
                tempNum /= 10;
                length++;
            }
            if (length > maxL) {// Проверка на максимальную длину
                maxL = length;
                maxNum = num;
            }
            if (length < minL) {// Проверка на минимальную длину
                minL = length;
                minNum = num;
            }
        }
        System.out.println("Самое длинное число: " + maxNum + " (длина: " + maxL + ")");
        System.out.println("Самое короткое число: " + minNum + " (длина: " + minL + ")");
//===========================================================================================
//Задача 4 Выводить только те числа которые идут в порядке возрастания
        System.out.println("\nЗадача № 4");
        System.out.println("Первый вариант решения, если нужно найти возрастание из всего массива:");
        int one = arr[0];
        System.out.print(one + " ");

        for (int i = 1; i < arr.length; i++) {
            int num = arr[i];
            if (num > one) {
                System.out.print(num + " ");
                one = num;
            }
        }
        System.out.println("\n\nВторой вариант решения, если надо все возрастания найти:");
        int ones = arr[0];
        System.out.print(ones);

        for (int i = 1; i < arr.length; i++) {
            int num = arr[i];
            if (num > ones) {
                System.out.print(" " + num);
            } else {
                System.out.println();
                System.out.print(num);
            }
            ones = num;
        }
    }
}
